def _test_spec(identifier: str) -> dict:
    return {
        "canvas": {"width": 640, "height": 360},
        "timeline": {"tracks": [{"order": 0, "items": [{"id": "motion", "kind": "motion", "placement": {"startFrame": 0, "durationFrames": 60}, "componentId": identifier, "props": {}}]}]},
    }


def _default_text(config: dict) -> str:
    candidates = [config.get("text"), config.get("recipe", {}).get("text") if isinstance(config.get("recipe"), dict) else None]
    recipe = config.get("recipe") if isinstance(config.get("recipe"), dict) else {}
    for key in ("texts", "crossing_texts"):
        if isinstance(recipe.get(key), list):
            candidates.append(" ".join(str(item.get("text", "")) for item in recipe[key] if isinstance(item, dict)))
    if isinstance(config.get("controls"), list):
        candidates.extend(item.get("value") for item in config["controls"] if item.get("type") == 6)
    return next((str(value).replace("\r", " ").replace("\n", " ").strip() for value in candidates if isinstance(value, str) and value.strip()), "")


def _palette(config: dict, family: str) -> tuple[str, str]:
    values = []

    def visit(value, key=""):
        if isinstance(value, dict):
            for child_key, child in value.items():
                visit(child, child_key.casefold())
            return
        if isinstance(value, list):
            if any(token in key for token in ("color", "fill", "top", "bottom", "glow", "highlight")) and len(value) in {3, 4} and all(isinstance(item, (int, float)) for item in value):
                scale = 255 if max(value) <= 1 else 1
                values.append("#" + "".join(f"{max(0, min(255, round(item * scale))):02x}" for item in value[:3]))
            for child in value:
                visit(child, key)

    visit(config)
    defaults = {
        "aesthetic-glow": ("#f6f0df", "#b026ff"),
        "apple-clean": ("#ffffff", "#8ca8ff"),
        "dimensional-metal": ("#e6e6e6", "#de3131"),
        "analog-glitch": ("#ffffff", "#ff285c"),
        "rainbow-trippy": ("#ffffff", "#d44bff"),
        "water-warp": ("#e8fbff", "#319dc7"),
        "elegant-misc": ("#fff7e6", "#b99152"),
        "simple-motion": ("#ffffff", "#7d8ea8"),
    }.get(family, ("#ffffff", "#808080"))
    return (values[0] if values else defaults[0], values[1] if len(values) > 1 else defaults[1])


def _family_motion(family: str, slug: str) -> str:
    if "GLITCH" in slug or "VHS" in slug or family == "analog-glitch":
        return "glitch"
    if family == "aesthetic-glow":
        return "glow"
    if family == "dimensional-metal":
        return "creed"
    if family == "rainbow-trippy":
        return "gradient"
    if family == "water-warp":
        return "morph"
    if "ZOOM" in slug or "REBOUND" in slug or "BOUNCE" in slug:
        return "pop"
    if any(token in slug for token in ("LEFT", "RIGHT", "ABOVE", "BOTTOM")):
        return "wipe"
    return "rise"


def _slug(value: str) -> str:
    return re.sub(r"[^a-z0-9]+", "-", value.casefold()).strip("-")


def _show(root: Path, revision: str, path: str) -> bytes:
    return _git(root, "show", f"{revision}:{path}")


def _git(root: Path, *args: str) -> bytes:
    result = subprocess.run(["git", "-C", str(root), *args], capture_output=True, check=False)
    if result.returncode:
        raise RuntimeError(result.stderr.decode(errors="replace").strip() or f"git {' '.join(args)} failed")
    return result.stdout


