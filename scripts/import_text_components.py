def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Import tracked VibeEdit text designs into the portable motion runtime")
    parser.add_argument("--source-root", type=Path, required=True)
    parser.add_argument("--revision", default="HEAD")
    args = parser.parse_args(argv)
    root = args.source_root.resolve()
    revision = _git(root, "rev-parse", args.revision).decode().strip()
    components = _registry_components(root, revision) + _mogrt_components(root, revision)
    if len(components) != 74:
        raise RuntimeError(f"reviewed portable text inventory changed: expected 74, found {len(components)}")
    if len({component["id"] for component in components}) != len(components):
        raise RuntimeError("portable text identifiers must be unique")

    manifest = {"schemaVersion": "1.0.0", "revision": revision, "components": components}
    (PACKAGE_ROOT / "catalog" / "motion-components.json").write_text(
        json.dumps(manifest, indent=2, ensure_ascii=False) + "\n",
        encoding="utf-8",
    )
    (PACKAGE_ROOT / "src" / "motion" / "components.generated.js").write_text(
        "// Generated by scripts/import_text_components.py from a tracked VibeEdit revision.\n"
        f"export const portableMotionComponents = Object.freeze({json.dumps(components, separators=(',', ':'), ensure_ascii=False)});\n",
        encoding="utf-8",
    )
    validation = _validate(components, revision)
    (PACKAGE_ROOT / "catalog" / "motion-validation.json").write_text(
        json.dumps(validation, indent=2, ensure_ascii=False) + "\n",
        encoding="utf-8",
    )
    _write_catalog(components)
    print(json.dumps({"ok": True, "revision": revision, "components": len(components), "families": validation["families"], "aggregateSha256": validation["aggregateSha256"]}))
    return 0


def _registry_components(root: Path, revision: str) -> list[dict]:
    paths = _git(root, "ls-tree", "-r", "--name-only", revision, SOURCE_PREFIX).decode().splitlines()
    registry_paths = sorted(path for path in paths if path.count("/") == SOURCE_PREFIX.count("/") + 2 and path.endswith("/registry-item.json"))
    components = []
    for path in registry_paths:
        metadata_bytes = _show(root, revision, path)
        metadata = json.loads(metadata_bytes)
        name = metadata["name"]
        if name in {"html-motion-mogrt", "learned-text-styles"}:
            continue
        if name not in STYLE:
            raise RuntimeError(f"portable style mapping missing for {name}")
        html_entry = next((item["path"] for item in metadata["files"] if item["path"].endswith(".html")), None)
        if not html_entry:
            raise RuntimeError(f"tracked component has no HTML source: {name}")
        html_path = f"{SOURCE_PREFIX}/{name}/{html_entry}"
        source_hash = hashlib.sha256(metadata_bytes + b"\0" + _show(root, revision, html_path)).hexdigest()
        motion, kind, default_text, foreground, accent = STYLE[name]
        components.append(
            {
                "id": f"vibeedit://text/{name}",
                "name": metadata["title"],
                "description": metadata["description"],
                "kind": kind,
                "family": "portable-catalog",
                "motion": motion,
                "defaultText": default_text,
                "palette": {"foreground": foreground, "accent": accent},
                "tags": list(dict.fromkeys([*metadata.get("tags", []), "portable", "deterministic"])),
                "source": {"path": html_path, "registryPath": path, "revision": revision, "sha256": source_hash},
            }
        )
    return components


def _mogrt_components(root: Path, revision: str) -> list[dict]:
    prefix = f"{SOURCE_PREFIX}/html-motion-mogrt/configs"
    paths = sorted(path for path in _git(root, "ls-tree", "-r", "--name-only", revision, prefix).decode().splitlines() if path.endswith(".json"))
    components = []
    for path in paths:
        source = _show(root, revision, path)
        config = json.loads(source)
        slug = config["slug"]
        family = config["family"]
        default_text = _default_text(config) or slug.replace("_", " ")
        foreground, accent = _palette(config, family)
        components.append(
            {
                "id": f"vibeedit://text/mogrt-{_slug(slug)}",
                "name": config.get("name", slug.replace("_", " ").title()),
                "description": f"Portable deterministic {family.replace('-', ' ')} adaptation of the tracked VibeEdit {slug} motion-title configuration.",
                "kind": "text",
                "family": family,
                "motion": _family_motion(family, slug),
                "defaultText": default_text,
                "palette": {"foreground": foreground, "accent": accent},
                "tags": ["text", "motion-title", "mogrt-adaptation", family, "portable", "deterministic"],
                "source": {"path": path, "revision": revision, "sha256": hashlib.sha256(source).hexdigest()},
            }
        )
    return components


def _validate(components: list[dict], revision: str) -> dict:
    node = shutil.which("node")
    if not node:
        raise RuntimeError("Node.js is required to validate generated text components")
    node_script = """
import { createHash } from 'node:crypto';
import { portableMotionComponents, renderComponent } from './src/index.js';
const digest = createHash('sha256');
for (const component of portableMotionComponents) {
  const context = { durationFrames: 60, width: 640, height: 360 };
  const early = renderComponent(component.id, {}, 2, context);
  const late = renderComponent(component.id, {}, 42, context);
  if (early !== renderComponent(component.id, {}, 2, context) || early === late || !early.includes('data-vibeedit-component=')) throw new Error(component.id);
  digest.update(component.id); digest.update(early); digest.update(late);
}
console.log(JSON.stringify({ count: portableMotionComponents.length, sha256: digest.digest('hex') }));
"""
    result = subprocess.run([node, "--input-type=module", "-e", node_script], cwd=PACKAGE_ROOT, capture_output=True, text=True, check=False)
    if result.returncode:
        raise RuntimeError(result.stderr.strip() or "JavaScript motion validation failed")
    node_result = json.loads(result.stdout)

    sys.path.insert(0, str(PYTHON_ROOT))
    importlib.invalidate_caches()
    for name in tuple(sys.modules):
        if name == "vibeedit.motion":
            del sys.modules[name]
    motion = importlib.import_module("vibeedit.motion")
    digest = hashlib.sha256()
    for component in motion.list_motion_components():
        spec = _test_spec(component["id"])
        early = motion.document_for_frame(spec, 2)
        late = motion.document_for_frame(spec, 42)
        if early != motion.document_for_frame(spec, 2) or early == late or "data-vibeedit-component=" not in early:
            raise RuntimeError(f"Python motion validation failed: {component['id']}")
        digest.update(component["id"].encode())
        digest.update(early.encode())
        digest.update(late.encode())
    families = {}
    for component in components:
        families[component["family"]] = families.get(component["family"], 0) + 1
    return {
        "schemaVersion": "1.0.0",
        "status": "passed",
        "revision": revision,
        "cases": len(components),
        "families": dict(sorted(families.items())),
        "runtime": {"python": sys.version.split()[0], "node": subprocess.run([node, "--version"], capture_output=True, text=True, check=True).stdout.strip()},
        "javascriptSha256": node_result["sha256"],
        "pythonSha256": digest.hexdigest(),
        "aggregateSha256": hashlib.sha256(f"{node_result['sha256']}:{digest.hexdigest()}".encode()).hexdigest(),
        "contract": "Every generated component rendered deterministically at two integer frames in both public runtimes without network or binary assets.",
    }


def _write_catalog(components: list[dict]) -> None:
    path = PACKAGE_ROOT / "catalog" / "catalog.json"
    catalog = json.loads(path.read_text(encoding="utf-8"))
    retained = [item for item in catalog["items"] if not item.get("provenance", {}).get("source", "").startswith(PROVENANCE_PREFIX) and item["id"] != "vibeedit://template/portable-motion-showcase"]
    catalog["items"] = retained + [_catalog_item(component) for component in components] + [_showcase_item()]
    path.write_text(json.dumps(catalog, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")


